#!/bin/bash

if [[ $# -eq 0 ]]; then
    BUILD=1
    INSTALL=1
else
    BUILD=0
    INSTALL=0
    for i in "$@"; do
        if [[ $i = "--build" ]]; then
            BUILD=1
        elif [[ $i = "--install" ]]; then
            INSTALL=1
        fi
    done
fi


echo "-=======================================-"
echo "-=== Kyle Hersey's Dialogc Installer ===-"
echo "-=======================================-"
echo " "
echo "This installer will unpack all source code"
echo "to the current working directory temporarily"
echo "until the build and installation are complete."
echo " "

if [[ $BUILD -eq 1 ]]; then
    echo "Proceed with Build? (Y/N)"
    echo -n "> "
    read proceed
    echo " "

    if [ $proceed != 'y' ] && [ $proceed != 'Y' ] && [ $proceed != 'yes' ] && [ $proceed != 'Yes' ] && [ $proceed != 'YES' ]; then
        echo "Build aborted..."
        exit
    fi

    uudecode $0

    echo "Unpacking Source Files..."
    tar -xzf dialogc.tar.gz

    echo "Building Source..."
    make -B >/dev/null #redirect make output so it doesn't show up

    if [[ $? -eq 0 ]]; then
        echo "Build Successful!"
        echo " "
    else
        echo "Build Failed"
        echo "Aborting Installation"
        exit
    fi
fi

if [[ $INSTALL -eq 1 ]]; then
    echo "Please enter installation directory "
    echo -n "> "
    read INSTALLPATH
    echo " "

    if [[ -d $INSTALLPATH ]]; then
        echo "Directory exists..."
        if [ ! -w $INSTALLPATH ]; then
            echo "Error: cannot write to $INSTALLPATH"
            exit
        fi
    else
        echo "Directory does not exist, create it? (Y/N)"
        read createpath
        if [ $createpath != 'y' ] && [ $createpath != 'Y' ] && [ $createpath != 'yes' ] && [ $createpath != 'Yes' ] && [ $createpath != 'YES' ]; then
            echo "Build aborted..."
            exit
        fi

        mkdir -p $INSTALLPATH

        if [[ $? -eq 0 ]]; then              #Check result of mkdir
            echo "$INSTALLPATH Created Successfully"
        else
            echo "$INSTALLPATH Creation Failed"
            echo "Aborting Installation"
            exit
        fi

    fi

    if [[ ! -d $INSTALLPATH/lib ]]; then
        mkdir $INSTALLPATH/lib
    fi

    if [[ ! -d $INSTALLPATH/bin ]]; then
        mkdir $INSTALLPATH/bin
    fi

    echo "Copying Files to $INSTALLPATH"
    cp -ar libJNIpm.so $INSTALLPATH/lib
    cp -ar yadc        $INSTALLPATH/bin
    cp -ar dialogc.jar $INSTALLPATH/bin
    cp -ar img         $INSTALLPATH
    cp dialogc.sh      $INSTALLPATH
    cp README          $INSTALLPATH
    cp pledge.txt      $INSTALLPATH

    #Clean Up
    echo "Cleaning Up Junk"
    rm -rf c_src
    rm -rf dialogc
    rm -rf yadc_src
    rm -rf img
    rm dialogc.jar
    rm yadc
    rm libJNIpm.so
    rm dialogc.sh
    rm pledge.txt
    rm README
    rm makefile
    rm dialogc.tar.gz

    echo "Installation Completed Successfully"

fi

echo "Quitting..."

exit


